#include "mutex.h"
/////////////////////////////////////////////////
mutex::mutex () {
    pthread_mutex_init(&mutexsum, NULL);
}
/////////////////////////////////////////////////
void mutex::lock() {
    pthread_mutex_lock (&mutexsum);
}
/////////////////////////////////////////////////
void mutex::tryLock() {
    pthread_mutex_trylock(&mutexsum);
}
/////////////////////////////////////////////////
void mutex::unlock() {
    pthread_mutex_unlock (&mutexsum);
}
/////////////////////////////////////////////////
mutex::~mutex() {
    pthread_mutex_destroy(&mutexsum);
}
